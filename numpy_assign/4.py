import numpy as np

a = np.array([1,2,3])
b = np.array([4,5,6])

print("Addition:")
print(a + b)

print("Subtraction:")
print(a - b)

print("Multiplication:")
print(a * b)

print("Division:")
print(a / b)

print("Power of 3:")
print(a ** 3)

print("Square:")
print(a ** 2)

print("Mean:")
print(np.mean(a))